    fun getVendor(bssid: String?): String {
        if (bssid.isNullOrEmpty()) return "Unknown"
        val cleanBssid = bssid.trim().lowercase()
        if (cleanBssid.length < 8) return "Unknown"
        val oui = cleanBssid.substring(0, 8)
        return vendorMap[oui] ?: "Unknown"
    }

    fun frequencyToChannel(freq: Int): Int {
        return when {
            freq == 2484 -> 14
            freq in 2407..2472 -> (freq - 2407) / 5 + 1
            freq in 5170..5825 -> (freq - 5170) / 5 + 34
            else -> 0
        }
    }
